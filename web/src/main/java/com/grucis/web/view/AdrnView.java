package com.grucis.web.view;

public final class AdrnView extends View {
  private int id;
  private int map;
  private int width;
  private int height;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public int getMap() {
    return map;
  }

  public void setMap(int map) {
    this.map = map;
  }

  public int getWidth() {
    return width;
  }

  public void setWidth(int width) {
    this.width = width;
  }

  public int getHeight() {
    return height;
  }

  public void setHeight(int height) {
    this.height = height;
  }
}
