package com.grucis.dev.model.export.sprite.animation;

public final class AnimationFrame {
  private int x;
  private int y;
  private int width;
  private int height;
  private int count;
  private int regX;
  private int regY;

  public int getX() {
    return x;
  }

  public void setX(int x) {
    this.x = x;
  }

  public int getY() {
    return y;
  }

  public void setY(int y) {
    this.y = y;
  }

  public int getWidth() {
    return width;
  }

  public void setWidth(int width) {
    this.width = width;
  }

  public int getHeight() {
    return height;
  }

  public void setHeight(int height) {
    this.height = height;
  }

  public int getCount() {
    return count;
  }

  public void setCount(int count) {
    this.count = count;
  }

  public int getRegX() {
    return regX;
  }

  public void setRegX(int regX) {
    this.regX = regX;
  }

  public int getRegY() {
    return regY;
  }

  public void setRegY(int regY) {
    this.regY = regY;
  }
}
